    <center><h1>Deltik Staff Expectations</h1></center>
    <h2>Everyone</h2>
    <p><ul><li>Always follow through on promise(s), or let it be known you are unable to keep your promise(s) as soon as you find this out.</li>
<li>Always treat clients with respect, even in the worst of situations. This includes:</li>
<ul>
<li>Dealing with abuse. Always ask, never tell. Do not assume malicious intent in communications, though plan for it in your actions.</li>
<li>Dealing with client inactivity. Always be helpful, and ask if the client wants you to do anything. Assume they want to do the task but are unable.</li>
<li>Dealing with client requests. Always reply as quickly as possible, with as much assistance as possible. This includes little things like:</li>
<li>Fetching information.</li>
<ul>
<li>Fetching helpful links.</li>
<li>Anticipating potential problems and providing solutions before they are encountered.</li>
<li>Asking the client to contact you if they need any further assistance.</li>
<li>Thanking the client for their time, and for choosing our hosting. (Whatever applies.)</li>
</ul>
<li>Always let the client know what is happening and when they can expect a problem to be resolved. (Even if it's not by you.)</li>
</ul>
<li>Always bring any questions to someone who has the answers. Never leave questions unanswered, unless you have been specifically denied access to that information. (ie, passwords, social insurance numbers, etc...)</li>
<li>Do your best to remain active and ready to assist.</li>
<li>Never perform an update to the site without first consulting Deltik. Exceptions are as follows:</li>
<ul>
<li>Updates outlined in your staff responsibilities in this document.</li>
<li>Updates which are minor, including:</li>
<ul>
<li>Typos to be fixed on the website. (Be sure you know these are typos and not just matters of preference.)</li>
<li>Fixing a small coding error.</li>
</ul>
<li>Updates which are not intended for public viewing, and have no chance of allowing unauthorized access to any resources. You are allowed to build test pages to test new ideas, provided you do not bring these online as part of the site.</li>
<li>Updates which are critical for the situation. Examples include:</li>
<ul>
<li>Fixing a coding error that is majorly affecting the functionality of the website.</li>
</ul>
</ul>
<li>If you have a problem, go to Deltik.</li></ul></p>
